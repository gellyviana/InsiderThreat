package br.imd.read;


public class User {
	
	private String name;
	private String userId;
	private String email;
	private String domain;
	private String role;
	
	public User(){
		
	}
	public String getName() {
		return name;
	}

	public String getUserId() {
		return userId;
	}

	public String getEmail() {
		return email;
	}

	public String getDomain() {
		return domain;
	}

	public String getRole() {
		return role;
	}

}
